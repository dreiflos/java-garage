    public void InicializarTabela() throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("#");
        modelo.addColumn("ID");
        modelo.addColumn("Modelo");
        modelo.addColumn("Marca");
        modelo.addColumn("Ano");
        modelo.addColumn("Preço");
        modelo.addColumn("Status");

        CarroDAO dao = new CarroDAO();
        List<carros> listar = dao.listar();
        int sequencia = 1;
        for (carros c : listar) {
            String st = "Á venda";
            if (c.getStatus() == 2) {
                st = "Reservado";
            } else if (c.getStatus() == 3) {
                st = "Vendido";
            }
            modelo.addRow(new Object[]{
                sequencia++,
                c.getId(),
                c.getModelo(),
                c.getMarca(),
                c.getAno(),
                c.getPreco(),
                st,
                String.format("R$ %,.2f", c.getPreco())
            });
        }

        jTable1.setModel(modelo);

        jTable1.removeColumn(jTable1.getColumnModel().getColumn(1));

        RowSorter<TableModel> sorter = new TableRowSorter<>(modelo);
        jTable1.setRowSorter(sorter);

        jTextField1.setText("");
    }

