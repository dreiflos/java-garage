    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            row = jTable1.convertRowIndexToModel(row);

            // Obtém o ID da coluna 1 (coluna 0 é a numeração sequencial)
            int id = Integer.parseInt(jTable1.getModel().getValueAt(row, 1).toString());

            int confirmar = JOptionPane.showConfirmDialog(
                    this,
                    "Deseja remover este carro?",
                    "Confirmação",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (confirmar == JOptionPane.YES_OPTION) {
                try {
                    CarroDAO dao = new CarroDAO();
                    boolean removido = dao.remover(id);

                    if (removido) {
                        JOptionPane.showMessageDialog(
                                this,
                                "Carro removido com sucesso!",
                                "Sucesso",
                                JOptionPane.INFORMATION_MESSAGE);
                        InicializarTabela();
                    } else {
                        JOptionPane.showMessageDialog(
                                this,
                                "Nenhum carro foi removido. Verifique o ID.",
                                "Aviso",
                                JOptionPane.WARNING_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Erro ao remover carro: " + e.getMessage(),
                            "Erro",
                            JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
                }
            }
        } else {
            JOptionPane.showMessageDialog(
                    this,
                    "Selecione um carro na tabela para remover.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try {
            String modelo = null;
            do {
                modelo = JOptionPane.showInputDialog(this, "Modelo:");
                if (modelo == null) {
                    return; // Usuário cancelou
                }
                if (modelo.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Modelo não pode estar vazio.");
                }
            } while (modelo.trim().isEmpty());

            String marca = null;
            do {
                marca = JOptionPane.showInputDialog(this, "Marca:");
                if (marca == null) {
                    return;
                }
                if (marca.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Marca não pode estar vazia.");
                }
            } while (marca.trim().isEmpty());

            int ano = -1;
            while (ano <= 0) {
                String inputAno = JOptionPane.showInputDialog(this, "Ano:");
                if (inputAno == null) {
                    return;
                }
                try {
                    ano = Integer.parseInt(inputAno);
                    if (ano <= 1900 || ano > java.time.Year.now().getValue() + 1) {
                        JOptionPane.showMessageDialog(this, "Ano inválido");
                        ano = -1;
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Ano deve ser um número válido");
                    ano = -1;
                }
            }

            double preco = -1;
            while (preco < 0) {
                String inputPreco = JOptionPane.showInputDialog(this, "Preço:");
                if (inputPreco == null) {
                    return;
                }
                try {
                    preco = parsePreco(inputPreco);
                    if (preco < 0) {
                        JOptionPane.showMessageDialog(this, "Preço deve ser positivo.");
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Digite um valor numérico válido para o preço. Use ponto ou vírgula como separador decimal.");
                    preco = -1;
                }
            }

            int status = 1;

            carros carro = new carros(0, modelo, marca, ano, preco, status);
            CarroDAO dao = new CarroDAO();
            dao.inserir(carro);
            InicializarTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

