    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // BOTÃO DE EDITAR
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um carro para editar.");
            return;
        }
        row = jTable1.convertRowIndexToModel(row);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int id = Integer.parseInt(model.getValueAt(row, 1).toString());
        String modelo = model.getValueAt(row, 2).toString();
        String marca = model.getValueAt(row, 3).toString();
        int ano = Integer.parseInt(model.getValueAt(row, 4).toString());

        Object value = model.getValueAt(row, 5);
        String precoStr;
        if (value instanceof Number) {
            precoStr = value.toString();
        } else {
            precoStr = value.toString()
                    .replace("R$", "")
                    .replace(".", "")
                    .replace(",", ".")
                    .trim();
        }
        double preco = Double.parseDouble(precoStr);

        String statusStr = model.getValueAt(row, 6).toString();
        int statusAtual;
        switch (statusStr) {
            case "Reservado":
                statusAtual = 2;
                break;
            case "Vendido":
                statusAtual = 3;
                break;
            default:
                statusAtual = 1; // "Á venda"
        }

        String novoModelo = (String) JOptionPane.showInputDialog(
                this, "Editar modelo:", "Editar Modelo",
                JOptionPane.PLAIN_MESSAGE, null, null, modelo);

        if (novoModelo == null || novoModelo.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Modelo não pode ser vazio");
            return;
        }

        String novaMarca = (String) JOptionPane.showInputDialog(
                this, "Editar marca:", "Editar Marca",
                JOptionPane.PLAIN_MESSAGE, null, null, marca);

        if (novaMarca == null || novaMarca.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Marca não pode ser vazia");
            return;
        }

        String strAno = (String) JOptionPane.showInputDialog(
                this, "Editar ano:", "Editar Ano",
                JOptionPane.PLAIN_MESSAGE, null, null, ano);

        int novoAno;
        try {
            novoAno = Integer.parseInt(strAno);
            if (novoAno <= 1900 || novoAno > java.time.Year.now().getValue() + 1) {
                JOptionPane.showMessageDialog(this, "Ano inválido");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ano deve ser um número válido");
            return;
        }

        String strPreco = (String) JOptionPane.showInputDialog(
                this, "Editar preço:", "Editar Preço",
                JOptionPane.PLAIN_MESSAGE, null, null, String.format("%.2f", preco));

        double novoPreco;
        try {
            novoPreco = Double.parseDouble(strPreco.replace(",", "."));
            if (novoPreco <= 0) {
                JOptionPane.showMessageDialog(this, "Preço deve ser positivo");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Preço inválido");
            return;
        }

        int st = StatusCarro.showStatusDialog(this);

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Confirmar atualização do carro?\n"
                + "Modelo: " + novoModelo + "\n"
                + "Marca: " + novaMarca + "\n"
                + "Ano: " + novoAno + "\n"
                + "Preço: R$ " + String.format("%.2f", novoPreco) + "\n"
                + "Status: " + (st == 1 ? "Á venda" : st == 2 ? "Reservado" : "Vendido"),
                "Confirmação",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {

                CarroDAO dao = new CarroDAO();
                carros carroAtualizado = new carros(id, novoModelo, novaMarca, novoAno, novoPreco, st);

                if (dao.atualizar(carroAtualizado)) {
                    JOptionPane.showMessageDialog(this, "Carro atualizado com sucesso!");
                    InicializarTabela();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Falha ao atualizar carro",
                            "Erro",
                            JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Erro ao atualizar: " + e.getMessage(),
                        "Erro",
                        JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um carro para editar.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

