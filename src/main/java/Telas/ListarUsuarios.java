    public void InicializarTabela() throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("#");
        modelo.addColumn("ID");
        modelo.addColumn("Nome");
        modelo.addColumn("Email");
        modelo.addColumn("Permissão");

        Banco dao = new Banco();
        List<usuario> lista = dao.Listar();
        int sequencia = 1;
        for (usuario c : lista) {
            modelo.addRow(new Object[]{
                sequencia++,
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getPermissao(),});
        }

        jTable1.setModel(modelo);
        RowSorter<TableModel> sorter = new TableRowSorter<>(modelo);
        jTable1.setRowSorter(sorter);
        jTable1.removeColumn(jTable1.getColumnModel().getColumn(1));
        jTextField1.setText("");
    }

