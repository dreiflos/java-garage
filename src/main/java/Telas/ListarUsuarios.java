    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:                                        
    int row = jTable1.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Selecione um usuário para editar.");
        return;
    }

    row = jTable1.convertRowIndexToModel(row);
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    int id = Integer.parseInt(model.getValueAt(row, 1).toString());
    String nome = model.getValueAt(row, 2).toString();
    String email = model.getValueAt(row, 3).toString();
    int permissao = (int) model.getValueAt(row, 4); 

    String novoNome;
    do {
        novoNome = (String) JOptionPane.showInputDialog(
                this, "Editar nome:", "Editar Nome",
                JOptionPane.PLAIN_MESSAGE, null, null, nome);

        if (novoNome == null) {
            return; 
        }
        if (novoNome.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome não pode ser vazio!");
        }
    } while (novoNome.trim().isEmpty());


    String novoEmail;
    boolean emailValido;
    do {
        emailValido = true;
        novoEmail = (String) JOptionPane.showInputDialog(
                this, "Editar email:", "Editar Email",
                JOptionPane.PLAIN_MESSAGE, null, null, email);

        if (novoEmail == null) {
            return;
        }
        if (novoEmail.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "E-mail não pode ser vazio!");
            emailValido = false;
        } else if (!novoEmail.contains("@")
                || novoEmail.indexOf("@") >= novoEmail.length() - 1
                || !novoEmail.substring(novoEmail.indexOf("@")).contains(".")) {
            JOptionPane.showMessageDialog(this,
                    "E-mail inválido! Deve conter '@' e um domínio (ex: usuario@dominio.com)");
            emailValido = false;
        }
    } while (!emailValido);

   
    String novaSenha = (String) JOptionPane.showInputDialog(
            this, "Nova senha (deixe em branco para manter a atual):",
            "Editar Senha", JOptionPane.PLAIN_MESSAGE);

  
    String mensagemConfirmacao = "Confirmar atualização do usuário?\n\n"
            + "ID: " + id + "\n"
            + "Nome: " + novoNome + "\n"
            + "E-mail: " + novoEmail + "\n"
            + "Permissão: " + permissao + "\n" 
            + (novaSenha != null && !novaSenha.trim().isEmpty()
            ? "Senha: ***** (alterada)\n"
            : "Senha: (mantida)\n");

    int confirm = JOptionPane.showConfirmDialog(
            this,
            mensagemConfirmacao,
            "Confirmação",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE);

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Banco dao = new Banco();
            usuario usuarioAtualizado = new usuario();
            usuarioAtualizado.setId(id);
            usuarioAtualizado.setNome(novoNome);
            usuarioAtualizado.setEmail(novoEmail);
            usuarioAtualizado.setPermissao(permissao); 

            if (novaSenha != null && !novaSenha.trim().isEmpty()) {
                usuarioAtualizado.setSenha(novaSenha);
            }

            dao.Atualizar(usuarioAtualizado);
            JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso!");
            InicializarTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao atualizar usuário: " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

