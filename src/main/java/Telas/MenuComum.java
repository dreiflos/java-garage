    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int pos = jTable1.getSelectedRow();
        if (pos >= 0) {
            pos = jTable1.convertRowIndexToModel(pos);
            String modelo = jTable1.getModel().getValueAt(pos, 2).toString();
            String idCarro = jTable1.getModel().getValueAt(pos, 1).toString();

            int ok = JOptionPane.showConfirmDialog(this, "Deseja realmente comprar o " + modelo + "?",
                    "Confirmação de Compra", JOptionPane.YES_NO_OPTION);

            if (ok == JOptionPane.YES_OPTION) {
                try {
                    Connection con = Conexao.abrirConexao();
                    PreparedStatement ps = con.prepareStatement("UPDATE carros SET status = 2 WHERE id = ?");
                    ps.setInt(1, Integer.parseInt(idCarro));
                    int rowsAffected = ps.executeUpdate();

                    if (rowsAffected > 0) {
                        DefaultTableModel modeloTable = (DefaultTableModel) jTable1.getModel();
                        modeloTable.removeRow(pos);

                        JOptionPane.showMessageDialog(this, "Compra realizada com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao atualizar o status do carro",
                                "Erro", JOptionPane.ERROR_MESSAGE);
                    }

                    ps.close();
                    con.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Erro ao conectar com o banco de dados: " + e.getMessage(),
                            "Erro", JOptionPane.ERROR_MESSAGE);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "ID do carro inválido",
                            "Erro", JOptionPane.ERROR_MESSAGE);
                } catch (Exception ex) {
                    System.getLogger(MenuComum.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro: Selecione um carro para comprar",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

