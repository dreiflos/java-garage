    public void InicializarTabela() throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("#");
        modelo.addColumn("ID");
        modelo.addColumn("Modelo");
        modelo.addColumn("Marca");
        modelo.addColumn("Ano");
        modelo.addColumn("Preço");
        modelo.addColumn("Status");
        String st = "Á venda";

        // Buscar do banco de dados
        CarroDAO dao = new CarroDAO();
        List<carros> listarcomun = dao.listarcomun();
        int sequencia = 1;
        for (carros c : listarcomun) {
            modelo.addRow(new Object[]{
                sequencia++,
                c.getId(),
                c.getModelo(),
                c.getMarca(),
                c.getAno(),
                c.getPreco(),
                st,});
        }

        jTable1.setModel(modelo);
        jTextField1.setText("");

        RowSorter<TableModel> sorter = new TableRowSorter<>(modelo);
        jTable1.setRowSorter(sorter);
        jTable1.removeColumn(jTable1.getColumnModel().getColumn(1));
    }

