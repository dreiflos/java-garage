    /**
     * @param args the command line arguments
     */
    private void Pesquisar() {
        DefaultTableModel modelo
                = (DefaultTableModel) jTable1.getModel();
        TableRowSorter filtro = new TableRowSorter(modelo);
        String buscar = jTextField1.getText();
        if (buscar.length() == 0) {
            filtro.setRowFilter(null);
        } else {
            filtro.setRowFilter(RowFilter.regexFilter(buscar));
        }
        jTable1.setRowSorter(filtro);
    }

    private void ConfigurarCampoPesquisa() {
        jTextField1.getDocument().addDocumentListener(
                new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                Pesquisar();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                Pesquisar();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                Pesquisar();
            }
        }
        );

    }

